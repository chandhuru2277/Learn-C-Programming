#include <stdio.h>

int main(void)
{
    // local variable a, b
    int a = 10;
    int b = 20;
    // print initialize value
    printf("\na: %d", a);
    printf("\nb: %d", b);

    a = 30;
    b = 40;

    // print updated value
    printf("\na: %d", a);
    printf("\nb: %d\n", b);

    return 0;
}

